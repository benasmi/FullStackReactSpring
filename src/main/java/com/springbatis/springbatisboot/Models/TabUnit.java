package com.springbatis.springbatisboot.Models;

public class TabUnit{
    public int line;
    public int bar;


    public TabUnit(int line, int bar) {
        this.line = line;
        this.bar = bar;
    }

    public int getLine() {
        return line;
    }

    public void setLine(int line) {
        this.line = line;
    }

    public int getBar() {
        return bar;
    }

    public void setBar(int bar) {
        this.bar = bar;
    }
}
